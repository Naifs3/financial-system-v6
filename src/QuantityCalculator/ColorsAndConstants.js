export const calculateArea = (typeId, length, width, height, quantity = 1) => {
  const l = parseFloat(length) || 0;
  const w = parseFloat(width) || 0;
  const h = parseFloat(height) || 0;
  const q = parseFloat(quantity) || 1;

  switch(typeId) {
    case 'floor':
    case 'ceiling':
      return l * w;
    case 'walls4':
      return (l + w) * 2 * h;
    case 'wallSingle':
      return l * h;
    case 'wazra':
      return (l + w) * 2;
    case 'unit':
      return q;
    default:
      return l * w;
  }
};

